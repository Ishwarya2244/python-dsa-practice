##🔹 What is Array Insertion?
##
##Array insertion means adding a new element at:
##1️⃣ End
##2️⃣ Beginning
##3️⃣ Specific index
##
##append=end
##insert=beginning


#1.Q1️⃣ Insert 50 at end
arr=[5,10,15]
arr.append(50)
print(arr)

#2.Q2️⃣ Insert 1 at beginning
arr=[3,6,9]
arr.insert(0,1)
print(arr)


#Q3️⃣ Insert 100 at index 2
arr=[10,20,30,40]
arr.insert(2,100)
print(arr)
